// Aula 1 – Variáveis, operadores e entrada/saída básicas
// Exemplo: Operadores relacionais

#include<stdio.h>

int main(){
    int n = 20;
    printf("n == 20: %d\n", n == 20); // 1 (verdadeiro)
    printf("n != 15: %d\n", n != 15); // 1 (verdadeiro)
    printf("n > 25:  %d\n", n > 25);  // 0 (falso)
    printf("n < 30:  %d\n", n < 30);  // 1 (verdadeiro)
    printf("n >= 20: %d\n", n >= 20); // 1 (verdadeiro)
    printf("n <= 19: %d\n", n <= 19); // 0 (falso)
    return 0;
}
